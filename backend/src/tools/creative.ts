// LangChain Tool wraps native tool-call args as { input: "<json-string>" }.
// This helper unwraps that envelope so _call always gets the real params.
function parseToolInput(raw: string): Record<string, unknown> {
  const outer = JSON.parse(raw);
  if (typeof outer.input === "string") return JSON.parse(outer.input);
  return outer;
}

export class GenerateImageTool extends Tool {
  name = "generate_image";
  description =
    "Generate an image concept/description using Gemini. Input: JSON with 'prompt' describing the image to generate. Returns the image description and metadata ready for NFT use.";

  private gemini = getLLM("gemini-image");

  async _call(input: string): Promise<string> {
    let prompt = input;
    try {
      const parsed = parseToolInput(input);
      prompt = (parsed.prompt as string) || (parsed.input as string) || input;
    } catch {}

    logger.info("LLM", "[Yasmin] generate_image: invoking Gemini", { model: "gemini-2.5-flash", promptPreview: prompt.slice(0, 150) });
    const response = await this.gemini.invoke([
      {
        role: "user",
        content: `You are an expert NFT artist. Generate a detailed visual description for the following image concept. Provide the description in a structured JSON format with fields: title, visual_description, color_palette, art_style, and suggested_filename.\n\nConcept: ${prompt}`,
      },
    ]);

    const content =
      typeof response.content === "string"
        ? response.content
        : JSON.stringify(response.content);

    logger.info("LLM", "[Yasmin] generate_image: Gemini responded", { contentPreview: content.slice(0, 300) });
    // Upload description to Lighthouse for on-chain reference
    const cid = await lighthouseService.upload(
      JSON.stringify({ prompt, gemini_output: content, generated_by: "Yasmin via gemini-2.5-flash" })
    );
    logger.info("IPFS", "[Yasmin] generate_image: metadata uploaded to Lighthouse", { cid, url: lighthouseService.getGatewayUrl(cid), promptPreview: prompt.slice(0, 100) });

    return JSON.stringify({
      prompt,
      description: content,
      imageCID: cid,
      url: lighthouseService.getGatewayUrl(cid),
      note: "Image description generated by Gemini 2.5 Flash. Use imageCID with create_nft_metadata to complete NFT.",
    });
  }
}

export class CreateNFTMetadataTool extends Tool {
  name = "create_nft_metadata";
  description =
    "Create NFT metadata JSON. Input: JSON with 'name', 'description', 'imageCID' fields. Returns metadata CID.";

  async _call(input: string): Promise<string> {
    let params: Record<string, unknown> = {};
    try {
      params = parseToolInput(input);
    } catch {
      return JSON.stringify({ error: "Invalid JSON input for create_nft_metadata" });
    }
    const name = params.name as string | undefined;
    const description = params.description as string | undefined;
    const imageCID = (params.imageCID ?? params.image_cid ?? params.cid) as string | undefined;
    const attributes = Array.isArray(params.attributes) ? params.attributes : [];
    const metadata = {
      name,
      description,
      image: imageCID ? `ipfs://${imageCID}` : undefined,
      attributes,
      created_by: "Yasmin - Vantage Labs",
    };
    const cid = await lighthouseService.upload(JSON.stringify(metadata));
    logger.info("IPFS", "[Yasmin] create_nft_metadata: metadata uploaded", { cid, name, url: lighthouseService.getGatewayUrl(cid) });
    return JSON.stringify({ cid, metadata, url: lighthouseService.getGatewayUrl(cid) });
  }
}

